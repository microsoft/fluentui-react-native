/**
 * Token-name metadata.
 *
 * Reachable only via the `./metadata` subpath export (see package.json) —
 * deliberately NOT re-exported from `./index.ts`. The package root ships
 * types only, so consumers importing types still pay zero runtime cost;
 * tooling that needs to enumerate every theme token (an analyzer
 * building a sentinel test theme, a documentation generator, etc.)
 * imports `'@fluentui-react-native/theme-types/metadata'` and gets these
 * tuples without polluting the type-only surface.
 *
 * If you add or remove a key in `Color.types.ts`, `Typography.types.ts`,
 * `Shadow.types.ts`, or `Theme.types.ts`, update the corresponding tuple
 * here. Drift is caught by the parity tests in
 * `src/__tests__/metadata.test.ts` — they fail at `tsgo` time when the
 * tuple no longer matches the interface.
 */

