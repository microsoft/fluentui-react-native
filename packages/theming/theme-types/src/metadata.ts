/**
 * Every named color key reachable through `Theme['colors']`. The
 * underlying `ThemeColorDefinition` has an open index signature
 * (`[customColor: string]: ColorValue`), so this tuple enumerates the
 * statically-declared keys only — that's the surface that matters for
 * code generation and analyzer coverage.
 *
 * The order here mirrors the source interfaces (text → background →
 * control → alias) so a key's neighbors tell you which interface owns it.
 */
export const COLOR_TOKEN_NAMES = [
  ...PALETTE_TEXT_COLOR_NAMES,
  ...PALETTE_BACKGROUND_COLOR_NAMES,
  ...CONTROL_COLOR_TOKEN_NAMES,
  ...ALIAS_COLOR_TOKEN_NAMES,
] as const;

export type ColorTokenName = (typeof COLOR_TOKEN_NAMES)[number];

